CREATE TABLE `usermanage-login`.`users` ( `id` INT NOT NULL AUTO_INCREMENT , `name` VARCHAR(100) NOT NULL , `first_name` VARCHAR(50) NOT NULL , `last_name` VARCHAR(50) NOT NULL , `email` VARCHAR(50) NOT NULL , `password` VARCHAR(50) NOT NULL , `number` VARCHAR(50) NOT NULL , PRIMARY KEY (`id`)) ENGINE = InnoDB;



